    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nome = textField1.getText();
        String idade = textField2.getText();
        String endereco = textField3.getText();
        String sexo = textField4.getText();
        String altura = textField5.getText();
        String telefone = textField6.getText();
        String bolsa = textField7.getText();
        String hora_servico = textField8.getText();

        DefaultTableModel tabelaEstagiario = (DefaultTableModel) tbEstagiarios.getModel();
        Object[] novoEstagiario = new Object[]{
            nome,
            idade,
            endereco,
            sexo,
            altura,
            telefone,
            bolsa,
            hora_servico,
        };
        tabelaEstagiario.addRow(novoEstagiario);
        //limpa
        textField1.setText("");
        textField2.setText("");
        textField3.setText("");
        textField4.setText("");
        textField5.setText("");
        textField6.setText("");
        textField7.setText("");
        textField8.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tbEstagiarios.getSelectedRow();
        if(linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione uma linha");
        } else {
            DefaultTableModel tabelaEstagiario = (DefaultTableModel) tbEstagiarios.getModel();
            tabelaEstagiario.removeRow(linhaSelecionada);
            JOptionPane.showMessageDialog(this, "Estagiário excluído com sucesso!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

